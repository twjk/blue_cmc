package com.qcmz.cmc.proxy.spider.weixin.vo;

public class OfficialAccountBean {
	private String fakeid;
	private String nickname;
	private String alias;
	private String round_head_img;
	private String service_type;
	private String signature;
	public String getFakeid() {
		return fakeid;
	}
	public void setFakeid(String fakeid) {
		this.fakeid = fakeid;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public String getRound_head_img() {
		return round_head_img;
	}
	public void setRound_head_img(String round_head_img) {
		this.round_head_img = round_head_img;
	}
	public String getService_type() {
		return service_type;
	}
	public void setService_type(String service_type) {
		this.service_type = service_type;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
}
